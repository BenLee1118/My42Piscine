int	ft_atoi(char *str)
{
	int	i;
	int	sign;
	int	n;

	i = 0;
	sign = 1;
	n = 0;
	if (str[i] != '\0')
	{
		while ((str[i] == ' ') || (9 <= str[i] && str[i] <= 13))
			i++;
		while ((str[i] == '+') || (str[i] == '-'))
		{
			if (str[i] == '-')
				sign *= -1;
			i++;
		}
		while ('0' <= str[i] && str[i] <= '9')
		{
			n = n * 10 + (str[i] - '0');
			i++;
		}
		return (n * sign);
	}
	return (0);
}
